                            <p>Le titulaire du bac pro MSPC assure la maintenance corrective et préventive d'installations à caractère industriel. Il participe aussi bien à l'amélioration et à la modification de matériels existants qu'à la mise en œuvre de nouveaux équipements. Il intervient sur les parties opératives et sur les parties commandes des installations. Il doit impérativement respecter les règles de santé et de sécurité, les normes de qualité et celles de protection de l'environnement.</p>
                            <p>Ce technicien répare ou dépanne les biens après avoir analysé leur fonctionnement. Son activité varie selon l'entreprise qui l'emploie, la nature et la complexité des équipements dont il a la charge. Il utilise les technologies d'aide au diagnostic et d'intervention. Il réalise les opérations de surveillance et/ou des opérations planifiées, signale les anomalies. Il communique avec le ou les utilisateurs des biens sur lesquels il travaille et avec les membres de son service.</p>
                            <p>Le diplômé peut être recruté par des entreprises appartenant à des secteurs économiques très variés. Rattaché au service maintenance, il peut intervenir seul ou en équipe. Il peut aussi encadrer tout ou partie d'une unité de maintenance.</p>
