                        <p>Le technicien en assurance prospecte la clientèle, analyse ses besoins et lui propose des produits d'assurance ou des produits financiers.</p>
                        <p>Il gère également les dossiers de sinistre et vérifie que la garantie est acquise. Il peut également être amené à définir la responsabilité et les éventuels recours à engager.</p>
                        <p>D'abord conseiller, téléconseiller, gestionnaire de contrats, technicien d'assurance ou collaborateur d'agence, le titulaire du BTS Assurance peut évoluer ensuite vers un emploi d'agent général ou de courtier d'assurances.</p>
