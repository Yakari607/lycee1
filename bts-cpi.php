                        <div class="formation-type">Formation sous statut scolaire ou par apprentissage</div>
                        <p>Le concepteur de produits industriels travaille en bureau d'études. Il modifie, améliore, reconçoit partiellement ou totalement des produits industriels.</p>
                        <p>Il travaille sur un poste de CAO qui lui permet d'effectuer des calculs, de créer un produit en 3D, et d'éditer des plans. Pour chaque étape de son projet, il tient compte des matériaux et des procédés de fabrication.</p>
