                    </div>

                    <div class="formation-card">
                        <h2>Les missions</h2>
                        <p>Le concepteur de produits industriels est un spécialiste de la conception de produits à dominante mécanique (engins de chantier, pièces de moteur, boîtes de vitesse, biens d'équipement et de consommation…).</p>
                        <p>Au terme de sa formation, il est en mesure, sous la responsabilité d'un ingénieur, de dessiner les prototypes, étudier leur coût puis faire les contrôles et les essais du produit avant sa fabrication.</p>
                        <p>Il peut intégrer des grandes entreprises, mais aussi des PME-PMI. Dans tous les cas, il s'inscrit dans un cadre collaboratif, soit au sein de l'entreprise, soit avec des partenaires ou des sous-traitants.</p>
