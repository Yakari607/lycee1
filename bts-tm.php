        // Script pour le carousel des atouts
        document.addEventListener('DOMContentLoaded', () => {
            const slides = document.querySelectorAll('.atout-slide');
            const indicators = document.querySelectorAll('.indicator');
            const prevBtn = document.querySelector('.carousel-control.prev');
            const nextBtn = document.querySelector('.carousel-control.next');
            
            let currentSlide = 0;
            const slideCount = slides.length;
            
            // Fonction pour afficher un slide spécifique
            function showSlide(index) {
                // Masquer tous les slides
                slides.forEach(slide => slide.classList.remove('active'));
                indicators.forEach(indicator => indicator.classList.remove('active'));
                
                // Afficher le slide demandé
                slides[index].classList.add('active');
                indicators[index].classList.add('active');
                
                // Mettre à jour l'index courant
                currentSlide = index;
            }
            
            // Event listeners pour les boutons de navigation
            prevBtn.addEventListener('click', () => {
                let newIndex = currentSlide - 1;
                if (newIndex < 0) newIndex = slideCount - 1;
                showSlide(newIndex);
            });
            
            nextBtn.addEventListener('click', () => {
                let newIndex = currentSlide + 1;
                if (newIndex >= slideCount) newIndex = 0;
                showSlide(newIndex);
            });
            
            // Event listeners pour les indicateurs
            indicators.forEach((indicator, index) => {
                indicator.addEventListener('click', () => {
                    showSlide(index);
                });
            });
            
            // Rotation automatique du carousel
            let autoSlideInterval = setInterval(() => {
                let newIndex = currentSlide + 1;
                if (newIndex >= slideCount) newIndex = 0;
                showSlide(newIndex);
            }, 5000);
            
            // Arrêter la rotation automatique si l'utilisateur interagit avec le carousel
            const carouselContainer = document.querySelector('.atouts-carousel-container');
            carouselContainer.addEventListener('mouseenter', () => {
                clearInterval(autoSlideInterval);
            });
            
            carouselContainer.addEventListener('mouseleave', () => {
                autoSlideInterval = setInterval(() => {
                    let newIndex = currentSlide + 1;
                    if (newIndex >= slideCount) newIndex = 0;
                    showSlide(newIndex);
                }, 5000);
            });
        });
    </script>
